int get_random(int min, int max) {
    return min + rand() % (max - min + 1);
}

void simulate_mouse_movement() {
    int delay = get_random(1000, 10000);
    mousehid.mouse_x = 0;
    mousehid.mouse_y = 0;
    USBD_HID_SendReport(&hUsbDeviceFS,(uint8_t*)&mousehid, sizeof (mousehid));
    HAL_Delay(delay);

    delay = get_random(0, 1000);
    mousehid.mouse_x = get_random(-10, 10);
    mousehid.mouse_y = get_random(-10, 10);
    uint32_t start = HAL_GetTick();
    while (HAL_GetTick()- start < delay) {
        mousehid.mouse_x += get_random(-2, 2);
        mousehid.mouse_y += get_random(-2, 2);
        USBD_HID_SendReport(&hUsbDeviceFS,(uint8_t*)&mousehid, sizeof (mousehid));
        HAL_Delay(10);
    }

}
